#int to float
#we can convert decimal, binary, octal & hexa to float number
print(float(10))
print(float(0b00111))
print(float(0o346))
print(float(0x94Face))

#Can't convert complex number to float number

#bool to float
print(float(True))
print(float(False))

#String to float
#String should be contain int value or float value with base 10
print(float("10"))
print(float("10.5"))